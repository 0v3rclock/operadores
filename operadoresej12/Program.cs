using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace operadoresej12
{
    internal class Program
    {
        static void Main(string[] args)
        {
            // Declarar variable de tipo entero (int)
            int edad = 25;
            Console.WriteLine("Valor de la variable 'edad': " + edad);

            // Declarar variable de tipo cadena (string)
            string nombre = "Juan Pérez";
            Console.WriteLine("Valor de la variable 'nombre': " + nombre);

            // Declarar variable de tipo decimal (double)
            double salario = 5000.50;
            Console.WriteLine("Valor de la variable 'salario': " + salario);

            // Declarar variable de tipo booleano (bool)
            bool isAdmin = true;
            Console.WriteLine("Valor de la variable 'isAdmin': " + isAdmin);

            // Declarar variable de tipo caracter (char)
            char inicial = 'J';
            Console.WriteLine("Valor de la variable 'inicial': " + inicial);

            Console.ReadKey();
        }
    }
}
